/**
 * 동일 isometric 스케일 (베이직 책장 154 → 70 렌더 = 0.4545 배).
 * 모든 가구가 같은 비율로 그려져 가로 폭이 자연스럽게 비례합니다.
 */
const SCALE = 70 / 154;

/** 카탈로그에서 마이룸으로 배치할 때 적용할 너비 (픽셀) */
export function getPlacementWidth(kind: FurnitureKind, variant: string): number {
  const natural = NATURAL_W[kind]?.[variant];
  if (natural === undefined) return Math.round(154 * SCALE); // fallback = 70
  return Math.round(natural * SCALE);
}
