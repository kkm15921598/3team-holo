/**
 * 두 좌표 사이 직선거리(미터). Haversine 공식.
 * use-geolocation.ts 의 distanceMeters 와 동일 구현 — 의존성 줄이려고 여기서도 정의.
 */
function distanceMeters(
  a: { lat: number; lng: number },
  b: { lat: number; lng: number },
): number {
  const R = 6371000;
  const phi1 = (a.lat * Math.PI) / 180;
  const phi2 = (b.lat * Math.PI) / 180;
  const dphi = ((b.lat - a.lat) * Math.PI) / 180;
  const dlambda = ((b.lng - a.lng) * Math.PI) / 180;
  const x =
    Math.sin(dphi / 2) ** 2 +
    Math.cos(phi1) * Math.cos(phi2) * Math.sin(dlambda / 2) ** 2;
  return 2 * R * Math.atan2(Math.sqrt(x), Math.sqrt(1 - x));
}

/**
 * 주어진 좌표에서 가장 가까운 N개 동을 거리순으로 반환.
 * 반환값은 동 정보 + 거리(미터).
 */
export function findNearestDongs(
  pos: { lat: number; lng: number },
  count = 3,
): Array<KoreanDong & { distanceM: number }> {
  return KOREAN_DONGS
    .map((d) => ({ ...d, distanceM: distanceMeters(pos, d) }))
    .sort((a, b) => a.distanceM - b.distanceM)
    .slice(0, count);
}
